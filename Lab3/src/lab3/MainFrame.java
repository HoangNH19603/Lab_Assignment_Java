    public void loadTable() {
        ArrayList<Student> arStudent = this.lStudents.select();
        dtm = (DefaultTableModel) tblSinhVien.getModel();
        clear();
        dtm.setRowCount(0);
        for(Student s : arStudent) {
            Object[] rowStudent = { s.getName(), s.getMarks(), s.getMajor(), s.getGrade(), s.isBonus() ? "Thưởng" : "Không" };
            dtm.addRow(rowStudent);
        }
    }

