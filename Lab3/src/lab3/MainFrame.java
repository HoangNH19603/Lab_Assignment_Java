    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        // TODO add your handling code here:
        int index = getItemSelectedIndex();
        if(index == -1) {
            return;
        }
        String name = tblSinhVien.getValueAt(index, 0).toString();
        String marks = tblSinhVien.getValueAt(index, 1).toString();
        int i = cbbMajor.getSelectedIndex();
        String grade = tblSinhVien.getValueAt(index, 3).toString();
        
        txtName.setText(name);
        txtGrade.setText(marks);
        cbbMajor.setSelectedIndex(i);
        txtHocLuc.setText(grade);
        chkThuong.setSelected(lStudents.select(index).isBonus());
    }//GEN-LAST:event_tblSinhVienMouseClicked

