    private Student createStudent() {
        String name = txtName.getText();
        String major = cbbMajor.getSelectedItem().toString();
        String strMarks = txtGrade.getText();
        if (name.equals("") || strMarks.equals("")) {
            JOptionPane.showMessageDialog(this, "Điền đầy đủ thông tin");
            return null;
        }
        Double marks = Double.parseDouble(strMarks);
        return new Student(marks, name, major);
    }
    
